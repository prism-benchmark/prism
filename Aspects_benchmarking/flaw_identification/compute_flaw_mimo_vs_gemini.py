# Main analysis
# ---------------------------------------------------------------------------

def analyse():
    """
    Returns:
      results[conf][rtype][model] = {
          "llm":   {Critical_Recall, Minor_Recall, nCPS, n},
          "human": {Critical_Recall, Minor_Recall, nCPS, n_reviews},
      }
    """
    results = defaultdict(lambda: defaultdict(dict))
    missing = []
    cps_available = []   # track which mimo runs have CPS data

    for conf in CONFERENCES:
        ids_50 = load_paper_ids(PAPER_IDS_50[conf])
        for rtype in LLM_TYPES:
            # ── Mimo ──────────────────────────────────────────────────────────
            mp = mimo_flaw_path(conf, rtype)
            if os.path.exists(mp):
                mimo_records = load_jsonl(mp)
                # Load optional CPS supplement (--mode cps_only output)
                cp = mimo_cps_path(conf, rtype)
                cps_records = load_jsonl(cp) if os.path.exists(cp) else None
                if cps_records:
                    cps_available.append(f"{conf}/{rtype} ({len(cps_records)} CPS records)")
                results[conf][rtype]["mimo"] = extract_metrics(mimo_records, ids_50,
                                                               cps_records=cps_records)
            else:
                results[conf][rtype]["mimo"] = None
                missing.append(f"MIMO {conf}/{rtype}: {mp}")

            # ── Gemini ────────────────────────────────────────────────────────
            gp = gemini_flaw_path(conf, rtype)
            if os.path.exists(gp):
                gemini_records = load_jsonl(gp)
                results[conf][rtype]["gemini"] = extract_metrics(gemini_records, ids_50)
            else:
                results[conf][rtype]["gemini"] = None
                missing.append(f"GEMINI {conf}/{rtype}: {gp}")

    if cps_available:
        print(f"\n[INFO] Mimo CPS data found for {len(cps_available)} runs:")
        for s in cps_available:
            print(f"  + {s}")
    else:
        print("\n[INFO] No Mimo CPS data found. Run --mode cps_only to compute Mimo nCPS.")

    return results, missing

# ---------------------------------------------------------------------------
# Aggregate (macro-avg over 5 conferences)
# ---------------------------------------------------------------------------

def compute_aggregate(results):
    """
    Returns:
      agg["human"][model][metric] = float  (macro-avg over confs, averaged over all llm runs)
      agg[rtype][model][metric]   = float
    """
    accum = defaultdict(lambda: defaultdict(lambda: defaultdict(lambda: defaultdict(list))))

    for conf in CONFERENCES:
        for rtype in LLM_TYPES:
            for model in ["mimo", "gemini"]:
                m = results[conf][rtype].get(model)
                if m is None:
                    continue
                for role in ["llm", "human"]:
                    role_m = m.get(role, {})
                    for metric in METRICS:
                        v = role_m.get(metric)
                        if v is not None:
                            accum[rtype][model][role][metric].append(v)

    def safe_mean(lst): return round(sum(lst) / len(lst), 4) if lst else None

    agg = {}
    for rtype in LLM_TYPES:
        agg[rtype] = {}
        for model in ["mimo", "gemini"]:
            agg[rtype][model] = {
                role: {metric: safe_mean(accum[rtype][model][role][metric]) for metric in METRICS}
                for role in ["llm", "human"]
            }
            agg[rtype][model]["n_confs"] = len(accum[rtype][model]["llm"].get("Critical_Recall", []))

    # Compute average Human across all rtype-runs
    for model in ["mimo", "gemini"]:
        buckets = defaultdict(list)
        for rtype in LLM_TYPES:
            for conf in CONFERENCES:
                m = results[conf][rtype].get(model)
                if m and m.get("human"):
                    for metric in METRICS:
                        v = m["human"].get(metric)
                        if v is not None:
                            buckets[metric].append(v)
        agg[f"__human__{model}"] = {metric: safe_mean(buckets[metric]) for metric in METRICS}

    return agg

