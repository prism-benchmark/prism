# Metrics extraction
# ---------------------------------------------------------------------------

def extract_metrics(records: List[dict], paper_ids: set,
                    cps_records: Optional[List[dict]] = None) -> Optional[Dict]:
    """
    Returns:
      {
        "llm":   {"Critical_Recall": float, "Minor_Recall": float, "nCPS": float|None, "n": int},
        "human": {"Critical_Recall": float, "Minor_Recall": float, "nCPS": float|None, "n_reviews": int},
      }

    nCPS sources:
      - Inline: from metrics_report.cps.Reviewer_Rankings (Gemini mode=all)
      - Merged: from cps_records (Mimo --mode cps_only → cps_results.jsonl)
    If neither source is available → nCPS = None.
    """
    filtered = [r for r in records if r.get("paper_id") in paper_ids]
    if not filtered:
        return None

    # Build nCPS lookup from separate cps_results.jsonl (mimo cps_only output)
    # Structure: {paper_id -> {reviewer_id -> nCPS}}
    external_ncps: Dict[str, Dict[str, float]] = {}
    if cps_records:
        for rec in cps_records:
            pid = rec.get("paper_id", "")
            if pid not in paper_ids:
                continue
            rankings = rec.get("metrics_report", {}).get("cps", {}).get("Reviewer_Rankings", [])
            if rankings:
                external_ncps[pid] = {e.get("Reviewer_ID", ""): e.get("nCPS") for e in rankings}

    llm_cr, llm_mr, llm_ncps = [], [], []
    human_cr, human_mr, human_ncps = [], [], []

    for rec in filtered:
        pid          = rec.get("paper_id", "")
        cfi_rankings = rec.get("metrics_report", {}).get("cfi", {}).get("Reviewer_Rankings", [])
        # Inline CPS (Gemini mode=all has it embedded)
        cps_rankings = rec.get("metrics_report", {}).get("cps", {}).get("Reviewer_Rankings", [])
        inline_ncps  = {e.get("Reviewer_ID", ""): e.get("nCPS") for e in cps_rankings}
        # External CPS (Mimo cps_results.jsonl)
        ext_ncps     = external_ncps.get(pid, {})
        # Merge: prefer inline, fall back to external
        ncps_map = {**ext_ncps, **inline_ncps}

        for entry in cfi_rankings:
            rid = entry.get("Reviewer_ID", "")
            cr  = entry.get("Critical_Recall")
            mr  = entry.get("Minor_Recall")
            ncp = ncps_map.get(rid)
            if rid == "LLM_Reviewer":
                if cr  is not None: llm_cr.append(cr)
                if mr  is not None: llm_mr.append(mr)
                if ncp is not None: llm_ncps.append(ncp)
            elif rid.startswith("Human_"):
                if cr  is not None: human_cr.append(cr)
                if mr  is not None: human_mr.append(mr)
                if ncp is not None: human_ncps.append(ncp)

    def safe_mean(lst): return round(sum(lst) / len(lst), 4) if lst else None

    return {
        "llm": {
            "Critical_Recall": safe_mean(llm_cr),
            "Minor_Recall":    safe_mean(llm_mr),
            "nCPS":            safe_mean(llm_ncps),
            "n": len(filtered),
        },
        "human": {
            "Critical_Recall": safe_mean(human_cr),
            "Minor_Recall":    safe_mean(human_mr),
            "nCPS":            safe_mean(human_ncps),
            "n_reviews": len(human_cr),
        },
    }

