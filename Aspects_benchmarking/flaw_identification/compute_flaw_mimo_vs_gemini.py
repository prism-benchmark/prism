def print_table(results, conf: str):
    sep = "-" * 84
    hdr = f"  {'Reviewer':<14} {'Model':<8} {'Role':<10}  {'CritRecall':>11}  {'MinorRecall':>11}  {'nCPS':>8}  {'n':>5}"
    print(f"\n  {conf.upper()}")
    print(f"  {sep}")
    print(f"{hdr}")
    print(f"  {sep}")
    for rtype in LLM_TYPES:
        for model in ["mimo", "gemini"]:
            m = results[conf][rtype].get(model)
            if m is None:
                print(f"  {rtype:<14} {model:<8}  [NO DATA]")
                continue
            for role, label in [("llm", "LLM"), ("human", "Human")]:
                rd = m.get(role, {})
                cr  = rd.get("Critical_Recall")
                mr  = rd.get("Minor_Recall")
                ncp = rd.get("nCPS")
                n   = rd.get("n") or rd.get("n_reviews", "?")
                cr_s  = f"{cr:.4f}"  if cr  is not None else "    N/A"
                mr_s  = f"{mr:.4f}"  if mr  is not None else "    N/A"
                ncp_s = f"{ncp:.4f}" if ncp is not None else "    N/A"
                print(f"  {rtype:<14} {model:<8} {label:<10}  {cr_s:>11}  {mr_s:>11}  {ncp_s:>8}  {n:>5}")
    print(f"  {sep}")


