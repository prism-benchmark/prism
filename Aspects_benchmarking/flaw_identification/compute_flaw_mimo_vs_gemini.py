def save_csvs(results, agg):
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    field_names = ["conference","reviewer","model","role","Critical_Recall","Minor_Recall","nCPS","n"]

    for conf in CONFERENCES:
        rows = []
        for rtype in LLM_TYPES:
            for model in ["mimo","gemini"]:
                m = results[conf][rtype].get(model)
                if not m: continue
                for role in ["llm","human"]:
                    rd = m.get(role,{})
                    rows.append({
                        "conference": conf, "reviewer": rtype, "model": model, "role": role,
                        "Critical_Recall": rd.get("Critical_Recall",""),
                        "Minor_Recall":    rd.get("Minor_Recall",""),
                        "nCPS":            rd.get("nCPS",""),
                        "n": rd.get("n") or rd.get("n_reviews",""),
                    })
        out = os.path.join(OUTPUT_DIR, f"flaw_{conf}.csv")
        with open(out, "w", newline="", encoding="utf-8") as f:
            w = csv.DictWriter(f, fieldnames=field_names); w.writeheader(); w.writerows(rows)
        print(f"  [SAVED] {out}")

    # Aggregate CSV
    rows = []
    for model in ["mimo","gemini"]:
        hm = agg.get(f"__human__{model}", {})
        rows.append({"conference":"ALL","reviewer":"human","model":model,"role":"human",
                     "Critical_Recall": hm.get("Critical_Recall",""),
                     "Minor_Recall": hm.get("Minor_Recall",""),
                     "nCPS": hm.get("nCPS",""),
                     "n": "all"})
    for rtype in LLM_TYPES:
        for model in ["mimo","gemini"]:
            m = agg.get(rtype,{}).get(model,{})
            if not m: continue
            ll = m.get("llm",{})
            rows.append({"conference":"ALL","reviewer":rtype,"model":model,"role":"llm",
                         "Critical_Recall": ll.get("Critical_Recall",""),
                         "Minor_Recall": ll.get("Minor_Recall",""),
                         "nCPS": ll.get("nCPS",""),
                         "n": m.get("n_confs","")})
    out = os.path.join(OUTPUT_DIR, "flaw_aggregate.csv")
    with open(out, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=field_names); w.writeheader(); w.writerows(rows)
    print(f"  [SAVED] {out}")

