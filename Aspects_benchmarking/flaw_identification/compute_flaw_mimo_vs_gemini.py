"""
compute_flaw_mimo_vs_gemini.py
==============================
Computes mean Flaw Identification metrics for:
  - Mimo  v2.5-pro  (50-paper subset, all conferences)
  - Gemini 2.5-flash-lite (50-paper subset filtered from ~200-paper runs)

Metrics extracted per paper per reviewer:
  - Critical_Recall
  - Minor_Recall
  (both from metrics_report.cfi.Reviewer_Rankings)

For Human: each run file contains Human_X entries alongside LLM_Reviewer.
  → Average all Human_X entries across all papers and all runs to get one Human mean.

Outputs:
  1. Per-conference tables + aggregate printed to console
  2. CSV files saved to flaw_identification/output_analysis/
  3. Bar chart PNG comparing Mimo vs Gemini
"""

