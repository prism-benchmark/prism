COLOR_GEMINI = "#3A6EA5"
COLOR_MIMO   = "#E07B39"
HATCH_MIMO   = "///"

ALL_REVIEWERS = ["human"] + LLM_TYPES
X_LABELS      = ["Human"] + LLM_LABELS


def _get_vals(agg, role_key, metric, model):
    vals = []
    for rtype in ALL_REVIEWERS:
        if rtype == "human":
            hm = agg.get(f"__human__{model}", {})
            vals.append(hm.get(metric))
        else:
            m = agg.get(rtype, {}).get(model, {}).get(role_key, {})
            vals.append(m.get(metric))
    return vals


def draw_subplot(ax, title, gem_vals, mimo_vals, y_label="Recall (0-1)"):
    n      = len(X_LABELS)
    bar_w  = 0.35
    x      = np.arange(n) * 0.95

    # Replace None with 0 for bar height (annotate only real values)
    gem_plot  = [v if v is not None else 0 for v in gem_vals]
    mimo_plot = [v if v is not None else 0 for v in mimo_vals]

    b_gem  = ax.bar(x - bar_w/2, gem_plot, width=bar_w,
                    color=COLOR_GEMINI, edgecolor="white", linewidth=0.6, zorder=3)
    b_mimo = ax.bar(x + bar_w/2, mimo_plot, width=bar_w,
                    color=COLOR_MIMO, edgecolor="white", linewidth=0.6,
                    hatch=HATCH_MIMO, zorder=3)

    for bars, vals in [(b_gem, gem_vals), (b_mimo, mimo_vals)]:
        for bar, val in zip(bars, vals):
            if val is None: continue
            ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.004,
                    f"{val:.3f}", ha="center", va="bottom",
                    fontsize=7.2, color="#222222")

    ax.set_xticks(x)
    ax.set_xticklabels(X_LABELS, fontsize=9)
    ax.set_title(title, fontsize=11, fontweight="bold", pad=8)
    ax.set_ylabel(y_label, fontsize=8.5)

    all_v = [v for v in gem_vals + mimo_vals if v is not None]
    ax.set_ylim(0, max(all_v) * 1.18 if all_v else 1.0)

    ax.yaxis.grid(True, linestyle="--", linewidth=0.6, alpha=0.7, zorder=0)
    ax.set_axisbelow(True)
    ax.spines["top"].set_visible(False)
    ax.spines["right"].set_visible(False)
    ax.tick_params(axis="y", labelsize=8)


def plot_charts(agg):
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    legend_handles = [
        mpatches.Patch(facecolor=COLOR_GEMINI, edgecolor="white", label="Gemini Flash Lite"),
        mpatches.Patch(facecolor=COLOR_MIMO,   edgecolor="white", hatch=HATCH_MIMO, label="Mimo v2.5 Pro"),
    ]

    # ── Figure 1: Critical + Minor Recall (Mimo vs Gemini, side-by-side) ──────
    fig1, axes1 = plt.subplots(1, 2, figsize=(15, 5))
    fig1.patch.set_facecolor("white")

    for idx, (metric, title) in enumerate([
        ("Critical_Recall", "Critical Recall"),
        ("Minor_Recall",    "Minor Recall"),
    ]):
        ax = axes1[idx]
        ax.set_facecolor("#F9F9F9")
        gem_h  = agg.get("__human__gemini", {}).get(metric)
        mimo_h = agg.get("__human__mimo",   {}).get(metric)
        gem_vals  = [gem_h]  + [agg.get(r,{}).get("gemini",{}).get("llm",{}).get(metric) for r in LLM_TYPES]
        mimo_vals = [mimo_h] + [agg.get(r,{}).get("mimo",  {}).get("llm",{}).get(metric) for r in LLM_TYPES]
        draw_subplot(ax, title, gem_vals, mimo_vals)

    fig1.legend(handles=legend_handles, fontsize=10, loc="upper right",
                bbox_to_anchor=(1.0, 1.0), framealpha=0.9, edgecolor="#cccccc")
    fig1.suptitle(
        "Flaw Identification (CFI): Gemini Flash Lite vs Mimo v2.5 Pro\n"
        "(All Conferences (ICLR2024/25/26 + ICML2025 + NeurIPS2025), 50 paper IDs per conference)",
        fontsize=12, fontweight="bold", y=1.03,
    )
    fig1.tight_layout()
    out1 = os.path.join(OUTPUT_DIR, "flaw_crit_minor_recall_comparison.png")
