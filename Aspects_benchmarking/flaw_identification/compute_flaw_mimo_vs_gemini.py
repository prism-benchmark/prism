    mr_d  = (hm_mimo.get("Minor_Recall")    - hm_gemini.get("Minor_Recall"))    if (hm_mimo.get("Minor_Recall") is not None    and hm_gemini.get("Minor_Recall") is not None)    else None
    ncp_d = (hm_mimo.get("nCPS")            - hm_gemini.get("nCPS"))            if (hm_mimo.get("nCPS") is not None            and hm_gemini.get("nCPS") is not None)            else None
    print(f"  {'Human':<14} {fmt_d(cr_d):>14}  {fmt_d(mr_d):>14}  {fmt_d(ncp_d):>10}")
    print(f"  {'-'*64}")

    for rtype in LLM_TYPES:
        mm = agg.get(rtype, {}).get("mimo",   {}).get("llm", {})
        gm = agg.get(rtype, {}).get("gemini", {}).get("llm", {})
        cr_d  = (mm.get("Critical_Recall") - gm.get("Critical_Recall")) if (mm.get("Critical_Recall") is not None and gm.get("Critical_Recall") is not None) else None
        mr_d  = (mm.get("Minor_Recall")    - gm.get("Minor_Recall"))    if (mm.get("Minor_Recall") is not None    and gm.get("Minor_Recall") is not None)    else None
        ncp_d = (mm.get("nCPS")            - gm.get("nCPS"))            if (mm.get("nCPS") is not None            and gm.get("nCPS") is not None)            else None
        print(f"  {rtype:<14} {fmt_d(cr_d):>14}  {fmt_d(mr_d):>14}  {fmt_d(ncp_d):>10}")
    print(f"  {sep}")

