def mimo_flaw_path(conf: str, rtype: str) -> str:
    suffix = "" if rtype == "sea" else f"_{rtype}"
    return os.path.join(FLAW_BASE, f"output_cfi_{conf}_mimo{suffix}", "all_papers_results.jsonl")


def mimo_cps_path(conf: str, rtype: str) -> str:
    """Path to cps_results.jsonl generated by --mode cps_only (may not exist yet)."""
    suffix = "" if rtype == "sea" else f"_{rtype}"
    return os.path.join(FLAW_BASE, f"output_cfi_{conf}_mimo{suffix}", "cps_results.jsonl")


def gemini_flaw_path(conf: str, rtype: str) -> str:
    top   = GEMINI_TOP_MAP[rtype]
    cup   = GEMINI_CONF_MAP[conf]
    inner = f"{rtype}_{conf}"
    return os.path.join(GEMINI_BASE, top, cup, "Flaw", inner, "all_papers_results.jsonl")

