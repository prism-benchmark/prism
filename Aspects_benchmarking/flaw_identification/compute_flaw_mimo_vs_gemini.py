def print_delta(agg):
    sep = "-" * 64
    hdr = f"  {'Reviewer':<14} {'CritRecall Δ':>14}  {'MinorRecall Δ':>14}  {'nCPS Δ':>10}"
    print(f"\n\n{'='*64}")
